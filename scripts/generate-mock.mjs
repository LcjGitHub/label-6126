/**
 * 为部首生成 5–8 个例字
 * @param {string} char
 * @param {string} pinyin
 * @param {string} meaning
 * @returns {Array<{ char: string; pinyin: string; meaning: string }>}
 */
function buildExamples(char, pinyin, meaning) {
  if (EXAMPLE_POOL[char]) {
    return EXAMPLE_POOL[char].map(([c, p, m]) => ({ char: c, pinyin: p, meaning: m }));
  }

  const count = 5 + (char.charCodeAt(0) % 4);
  const examples = [];
  for (let i = 0; i < count; i += 1) {
    const suffix = ['甲', '乙', '丙', '丁', '戊', '己', '庚', '辛'][i];
    examples.push({
      char: `${char}${suffix}`,
      pinyin: `${pinyin}${i + 1}`,
      meaning: `${meaning}部例字${i + 1}`,
    });
  }
  return examples;
}

const radicals = KANGXI_RADICALS.map(([char, pinyin, meaning, strokeCount], index) => ({
  id: index + 1,
  char,
  pinyin,
  meaning,
  strokeCount,
  examples: buildExamples(char, pinyin, meaning),
}));

